function getBase(muscleId) {
  return muscleId.replace(/_[LR]$/, '')
}

function getPainfulRegions(muscleState) {
  const painfulBases = new Set(
    Object.entries(muscleState)
      .filter(([, v]) => v.pain > 0)
      .map(([id]) => getBase(id))
  )
  const regions = new Set()
  for (const [region, prefixes] of Object.entries(REGIONS)) {
    for (const prefix of prefixes) {
      if ([...painfulBases].some(b => b === prefix || b.startsWith(prefix))) {
        regions.add(region)
        break
      }
    }
  }
  return regions
}

function getMaxPainEntry(muscleState) {
  let max = 0
  let name = ''
  for (const [id, v] of Object.entries(muscleState)) {
    if (v.pain > max) { max = v.pain; name = id }
  }
  return { pain: max, id: name }
}

export function getGlobalTips(muscleState) {
  const entries = Object.entries(muscleState).filter(([, v]) => v.pain > 0)
  const painCount = entries.length
  const maxEntry = getMaxPainEntry(muscleState)
  const maxPain = maxEntry.pain

  if (painCount === 0) {
    return [
      'Всё в порядке! Для профилактики: 5–10 мин растяжки после каждой тренировки снижают риск травм на 30%',
      'Регулярный сон 7–9 часов — основной фактор восстановления мышц',
    ]
  }

  const tips = []

  // Critical pain alert
  if (maxPain >= 8) {
    tips.push(`Интенсивная боль (${maxPain}/10) — прекратите нагрузку на эту зону; при боли в груди или иррадиации в руку немедленно обратитесь к врачу`)
  }

  // Region-specific tips
  const regions = getPainfulRegions(muscleState)
  for (const region of regions) {
    const regionTips = REGION_TIPS[region]
    if (regionTips) tips.push(...regionTips.slice(0, 2))
  }

  // Pattern-based systemic advice
  if (painCount >= 6) {
    tips.push('Множественные болевые зоны часто указывают на перетренированность или нарушение сна — рассмотрите разгрузочную неделю')
  }
  if (regions.has('neck') && regions.has('shoulder')) {
    tips.push('Боль в шее + плечах: типичная картина «офисного синдрома» — каждый час вставайте и делайте 2-минутную гимнастику')
  }
  if (regions.has('lowerBack') && regions.has('hip')) {
    tips.push('Поясница + ягодицы: часто связаны через ягодично-поясничную цепь — растяжка сгибателей бедра (выпад с наклоном) поможет обоим')
  }
  if (regions.has('leg') && regions.has('lowerLeg')) {
    tips.push('Боль по всей ноге: проверьте технику бега/приседаний и увеличьте время разминки до 10 мин')
  }

  // Mild overall pain advice
  if (maxPain <= 3 && painCount <= 2) {
    tips.push('Лёгкий дискомфорт после нагрузки — норма; используйте активное восстановление (лёгкая ходьба, плавание) вместо полного отдыха')
  }

  return tips.length ? tips : ['Умеренная нагрузка и регулярные растяжки — залог здоровья']
}
