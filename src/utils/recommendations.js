export function getRecommendations(muscleId) {
  // Strip _L / _R suffix for lookup
  const base = muscleId.replace(/_[LR]$/, '')
  return TIPS[base] ?? TIPS[muscleId] ?? ['Отдых и умеренная растяжка', 'Лёд при остром воспалении', 'Консультация врача при сильной боли']
}

